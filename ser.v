                                capacidade.abstração()    + \
                                capacidade.memoria()      + \
                                capacidade.compreensão()  + \
                                capacidade.conhecimento() + \
                                capacidade.comunicação()  + \
                                capacidade.aprender()     + \
                                controle.emocional()      + \
                                controle.planejamento()   + \
                                controle.soluções()

    inteligência.qi(ser.get_qi());
